    <p class="lead">
        Thank you for choosing to use LIMTS! This is a simple guide to allow you to start making use of the software immediately.<br>
    </p>

    <h3>Intro</h3>
    <p>LIMTS can be used for making class schedules, managing a list of professors, classes and buildings, and keeping track of changes in important information such as classroom availability and space restrictions, among other things.</p>
    <p>To begin, you need to first insert new data into LIMTS. This data can then be manipulated and used to make many decisions. </p>

    <h3>Adding Data</h3>
    <p>Let's start by adding a professor. To add a professor, click on the menubar and select "Add." You should see a dropdown menu. Then select the "Professor" option.</p>

    <p>Now that we have a professor added, go back to the Add menu and click on Class.</p>

    <p>Now that a class has been added, go back to the Add menu once more and click on Section. We can now combine the Professor and the class you added to LIMTS to make a new section, taught by the aforementioned professor at the meeting time you specify in the classroom(s) specified.</p>

    <h3>Managing Data</h3>
    <p>Say you made a mistake when you were adding a professor that you didn't catch until <i>after</i> you had submitted the form. To make corrections, click on the main menu and go to <q>Manage.</q> When you click on the Manage option, you will be directed to a page that gives you a submenu with a list of categories (e.g. Section, Class, Professor, Semester etc.). Click on the category you wish to edit/remove. Make whichever changes you want and click submit. A message should be displayed letting you know that your modifications were successful.
    </p>

    <h3>Viewing Data</h3>
    <p>We should now have at least one class, one section of a class, and one professor stored in LIMTS. As you make continue to add more information, you may want to see an aggregate of your data or the current schedule. LIMTS provides two ways of doing this:
    <ul>
    <li>The Table View</li>
    <li>The Calendar View</li>
    </ul>
    Both views allow you to specify parameters so you can look at only information you are interested in, and the calendar view allows you to look at a schedule.
    </p>
